class Solution
{
	public:
		string is_palindrome(int n)
		{
		   // Code here.
            int num = n;
            int rev = 0;
            
            while(num) {
                rev = (rev * 10) + ( num % 10);
                num = num / 10;
            }
            
            return n ==  rev ? "Yes" : "No";
		}
};

