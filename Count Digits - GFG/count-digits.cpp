class Solution{
public:
    int evenlyDivides(int N){
      int cnt = 0,orig = N;
        while(N){
            int rem = N%10;
            if(rem != 0 && orig % rem == 0) cnt++;
            N /= 10;
        }
        
        return cnt;
    }
};

